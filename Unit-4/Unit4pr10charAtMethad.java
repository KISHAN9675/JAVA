//WAP to extract and print the 3rd, 5th, and 7th characters from a given string using charAt()?
public class Unit4pr10charAtMethad {
    public static void main (String[] args){
        String str1 = "Programming";
        char a = str1.charAt(2);
        char b = str1.charAt(4);
        char c = str1.charAt(6);
        System.out.println(a + "\n" + b + "\n" + c);
    }
}